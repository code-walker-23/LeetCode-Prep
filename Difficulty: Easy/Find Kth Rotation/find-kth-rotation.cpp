class Solution {
  public:
    int findKRotation(vector<int> &arr) {
        // Code Here
        // when we do rotation k time , such that k == n , we got same array then rotation is 0 
        int low = 0;
        int high = arr.size()-1;
        
        int rotationCount = 0;
        while(arr[low] > arr[high])low++,rotationCount++;
        
        return rotationCount;
    }
};


